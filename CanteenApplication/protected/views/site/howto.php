<h1> How - to and Information </h1>
<hr/>

<h2> Main Functionality</h2>

<p> After being logged in you will notice the following tabs, How-to, Incoming Orders and Completed Orders. By default 
after login you will redirected to the Incoming orders tab.</p>

<h2> Incoming Orders</h2>

<p> In the incoming orders you shall see a table of incoming orders with the following headings (DeviceID, Item, Quantity, Time
Placed, Delivery Location and Status). The incoming orders are arranged by the newest order first. Canteen staff must update 
the status of the order by selecting one of the radio buttons beside it. </p>

<h2> Completed Orders</h2>

<p> In the completed orders you shall see a list of all completed orders that are placed that day.</p>

<h2> How - to</h2>

<p> Below are some quick how-to on how to use this application: <br /></p>

<ul>
<li>The incoming orders will be refreshed automatically every minute, so all that is required is to select the status of the 
order. <br/> </li>
<li>To view the completed order, simply click on the "Completed Orders" tab.</li>
</ul> 
